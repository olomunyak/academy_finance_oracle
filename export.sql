  SELECT B.BOARD_TXTNUM, B.MEM_ID, B.BOARD_CGY, B.BOARD_TIT, B.BOARD_CTN, B.BOARD_REGIDATE, B.BOARD_HITS, RANK() OVER(ORDER BY B.BOARD_TXTNUM ASC) AS RNK
